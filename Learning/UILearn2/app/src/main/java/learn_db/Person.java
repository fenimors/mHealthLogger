@Entity
public class Person {
    @NonNull
    @PrimaryKey String name;
    int age;
    String favoriteColor;
    public Person(String name, int age, String favoriteColor){
        this.name = name;
        this.age = age;
        this.favoriteColor = favoriteColor;
    }
}


